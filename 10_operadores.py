#operadores

#Asignación
x = 5 #asignación simple
print(x)
x += 2 #asignación con suma, equivalente a x = x + 2
print(x)
x -= 3 #asignación con resta, equivalente a x = x - 3
print(x)
x *= 4 #asignación con multiplicación, equivalente a x = x * 4
print(x)
x /= 2 #asignación con división, equivalente a x = x / 2
print(x)
x //= 3 #asignación con división entera, equivalente a x = x // 3
print(x)
x %= 2 #asignación con módulo, equivalente a x = x % 2
print(x)    
x **= 2 #asignación con potencia, equivalente a x = x ** 2
print(x)