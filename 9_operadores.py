#operadores aritméticos

x = 5
y = 2
print(x + y) #suma
print(x - y) #resta
print(x * y) #multiplicación
print(x / y) #división retorna flotante
print(x // y) #división entera
print(21 % 2) #módulo
#modulo es el resto de la división
print(x ** 2) #potencia

#precedencia de operadores
print(2 + 3 * 4) #multiplicación tiene mayor precedencia que la suma
print((2 + 3) * 4) #los paréntesis cambian la precedencia de los operadores

