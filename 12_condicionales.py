#condicionales
x=5
y=4
z= 5
if x > y and x > z:
    print("x es mayor que y, y también es mayor que z")
elif x == y and x == z:
    print("x es igual a y, y también es igual a z")
elif x < y or x < z:
    print("x es menor que y,o x es menor que z")
else:
    print("No se cumple ninguna de las condiciones anteriores")

